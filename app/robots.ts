import type { MetadataRoute } from "next";

export default function robots(): MetadataRoute.Robots {
  return {
    rules: [
      {
        userAgent: "*",
        allow: "/",
        disallow: ["/api/", "/chat/", "/subscription"],
      },
    ],
    sitemap: "https://gipiti.ru/sitemap.xml",
  };
}

